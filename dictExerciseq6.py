# Write a function that takes a category and computes 
# the average IMDB score (HINT: reuse the function 
# from question 2.)

def averageBasedOnCategory(movies, category):
    sum = 0
    count = 0
    for key in movies:
        if key['category'] == category:
            count += 1
            sum += key['imdb']
    average = sum/count
    print(f"IMDB SUM IS {sum}")
    return (average, count)

